// mathematical constants
let SQRT2 = Math.sqrt(2);
let PHI   = (1 + Math.sqrt(5)) / 2;
let TRIB  = 1.83928675521416;  // tribonacci constant
let SNUB  = Math.sqrt(2 + (4 * TRIB) - 2 * (TRIB * TRIB));  // used for snub cube (l) and snub cube (r)
let TRUNCUBE = 2 * (SQRT2 - 1); // used for truncated cube